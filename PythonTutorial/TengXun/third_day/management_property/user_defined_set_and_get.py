class Person:
    def __init__(self, name):
        self.name = name

    def set_name(self, name):
        self.name = name

    def get_name(self):
        return self.name

    def del_name(self):
        del self.name

    def set_age(self, age):
        self.age = age

    def get_age(self):
        return self.age

    def del_age(self):
        del self.age
